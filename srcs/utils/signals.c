#include "../../includes/minishell.h"

void	handle_sigint(int signo)
{
	(void)signo;
	write(STDOUT_FILENO, "\n", 1);
	rl_replace_line("", 0);
	rl_on_new_line();
	rl_redisplay();
}

void	ft_exit(t_shell *shell)
{
	cleanup_shell(shell);
	exit(shell->exit_status);
}

